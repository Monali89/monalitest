package StringFunctions;

public class   String_functions
{
 
	public static void main(String[] args)
	{
	String name="My name is Monali";
{
	System.out.println(name.length());//17	
	System.out.println(name);
	System.out.println(name.substring(0,2));//my
	System.out.println(name.substring(2, 7));//name
	System.out.println(name.substring(8, 11));//is
	System.out.println(name.substring(11, 17));//monali
}	
	}
	}
